/**
 * Buster shot projectile - decorative animated energy pellets
 */
export function BusterShot({ className = "" }: { className?: string }) {
  return (
    <div className={`flex items-center gap-1 ${className}`} aria-hidden="true">
      {[0, 1, 2].map((i) => (
        <motion.div
          key={i}
          className="rounded-full bg-[#00e5ff]"
          style={{
            width: 4 + i * 2,
            height: 4 + i * 2,
            boxShadow: `0 0 ${4 + i * 3}px #00e5ff, 0 0 ${8 + i * 4}px #00a8e8`,
          }}
          animate={{
            x: [0, 30 + i * 10],
            opacity: [1, 0],
          }}
          transition={{
            duration: 0.8,
            repeat: Infinity,
            delay: i * 0.2,
            ease: "easeOut",
            repeatDelay: 1.5,
          }}
        />
      ))}
    </div>
  );
}
